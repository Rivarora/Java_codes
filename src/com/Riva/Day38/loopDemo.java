package com.Riva.Day38;

public class loopDemo {
    public static void main(String[] args) {
        int x = 0;

        // while loop
        while (x > 0) {
            System.out.println("While Loop: " + x);
        }

        // do-while loop
        do {
            System.out.println("Do-While Loop: " + x);
        }
        while (x > 0);
    }
}

