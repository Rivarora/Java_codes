package com.Riva.Day40;

public class SquarePattern {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {   // rows
            for (int j = 0; j < 3; j++) { // columns
                System.out.print("# ");
            }
            System.out.println(); // new line
        }
    }
}
