package com.Riva.Day40;

public class numberPattern {
    public static void main(String[] args) {
        for (int i = 0; i < 4; i++) {  // 4 rows
            for (int j = 1; j <= 5; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
